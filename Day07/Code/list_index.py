# 练习:(list_index.py)
#         已知有列表:
#             L = [3.5]
#         用索引的切片等操作,将原列表的改变为:
#             L = [1,2,3,4,5,6]
#         将列表反转,删除最后一个元素后,打印此列表:
#             print(L)    #[6,5,4,3,2]

L = [3,5]
L[0:0] = [1,2]
print(L)

L [3:3] = [4]
print(L)

L [5:] = [6]
print(L)
print(id(L))        

#L = L[::-1]         #新建的列表,重新赋值给L
L[::] = L[::-1]     #新建的列表转到之前的列表,id 不变
print(L)
print(id(L))        #会变化

del L[-1]
print(L)

